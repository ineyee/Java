public class Operator {

	public static void main(String[] args) {
		// 大多数的运算符跟其他语言里都是一样的，下面我们只讲几个比较特殊的。
		字符串拼接();
	}

	static void 字符串拼接() {
		/*
		 * Java里可以用加号把若干变量拼接成一个字符串。
		 */
		String name = "张三";
		int age = 18;
		double height = 1.88;
		System.out.println("我的名字是：" + name + "，年龄是：" + age + "，身高是：" + height);
	}
	
}
